use nes_core::{cpu6502::CPU, Bus, Tick};

pub struct Game {
    pub cpu: CPU<GameBus>,
}

impl Game {
    pub fn new(bus: GameBus) -> Box<Self> {
        let start_address = 0x0600u16;
        let cpu = CPU::new(bus, start_address);
        let game = Self { cpu };
        Box::new(game)
    }
    pub fn step(&mut self) {
        self.cpu.tick();
        // render(&mut self.cpu.bus.output);
    }
}

pub struct GameBus {
    memory: [u8; 0xFFFF],
    rng: Box<dyn Fn()->u8>,
    btn: Box<dyn Fn()->u8>,
    render: Box<dyn Fn(&[u8])->()>,
}

impl GameBus {
    pub fn new(
        rng: impl Fn()->u8 + 'static,
        btn: impl Fn()->u8 + 'static,
        render: impl Fn(&[u8])->() + 'static,
    ) -> Self {
        let mut memory = [0u8; 0xFFFF];
        memory[0x0600..(0x0600 + GAME_CODE.len())].copy_from_slice(&GAME_CODE);
        let rng = Box::new(rng);
        let btn = Box::new(btn);
        let render = Box::new(render);
        Self {
            memory,
            rng,
            btn,
            render,
        }
    }
}

impl Bus for GameBus {
    fn read(&self, address: u16) -> u8 {
        if address == 0x00FE {
            (self.rng)()
        } else if address == 0x00FF {
            (self.btn)()
        } else {
            self.memory[address as usize]
        }
    }

    fn write(&mut self, address: u16, data: u8) {
        self.memory[address as usize] = data;
        if 0x0200 <= address && address < 0x0600 {
            (self.render)(&mut self.memory[0x0200..0x0600]);
        }
    }
}

